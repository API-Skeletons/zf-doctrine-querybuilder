<?php

namespace ZF\Apigility\Doctrine\Server\Collection\Filter\ORM;

class On extends AbstractFilter
{
    public function filter($queryBuilder, $metadata, $option)
    {
        if (isset($option['where'])) {
            if ($option['where'] == 'and') {
                $queryType = 'andWhere';
            } elseif ($option['where'] == 'or') {
                $queryType = 'orWhere';
            }
        }

        if (!isset($queryType)) {
            $queryType = 'andWhere';
        }

        if (!isset($option['alias'])) {
            $option['alias'] = 'row';
        }

        $format = null;
        if (isset($option['format'])) {
            $format = $option['format'];
        }

        $queryBuilder->$queryType($queryBuilder->expr()->eq($option['alias'] . '.' . $option['field'], $option['value']));
    }
}
